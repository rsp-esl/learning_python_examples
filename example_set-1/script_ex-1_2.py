## String manipulation

# print a text or string (starting/ending
# with double quotes or single quotes).

print ("This is a string.")

# print multiple strings in the same line: with commas (,) separated
print ("Hello", "world", '!')

# print multiple strings in the same line: appending strings (+)
print ("Hello " + "world" + '!')
print ('Hello ' + 'world' + '!')
print ("\"Hello " + "world" + "!\"")
print ('"Hello ' + 'world' + '!"') 
print ('\"Hello ' + 'world' + '!\"') 


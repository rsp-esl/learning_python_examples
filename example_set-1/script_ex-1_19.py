from __future__ import print_function # for Python 2.6 or higher

## String manipulation

s = '1'
print (s.isdigit())    # output: True
print ('50'.isdigit()) # output: True
print ('a0'.isdigit()) # output: False
print ('-1'.isdigit()) # output: False
print ('a0'.isalpha()) # output: False
print ('a0'.isalnum()) # output: True


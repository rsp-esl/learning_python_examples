from __future__ import print_function # for Python 2.6 or higher

## String, varaible-value assignment

# assign strings to variables (multi-variable assignment)
x, y, z = 'one', 'two', 3

print ('x = {:s}'.format(x) )    # x = one
print ('y = {:s}'.format(y) )    # y = two
print ('z = {:d}'.format(z) )    # z = 3
print ('x=%s, y=%s, z=%d' % (x, y, z)) # x=one, y=two, z=3


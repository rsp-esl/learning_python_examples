## String manipulation

# append a string to another string by using '+'
s = "Hello"
s += ' '
s += "world!"

print ("s = " + s  )  
print ("type(s) = " + str(type(s)) )  # show type of variable s
print ("length  = " + str(len(s)) )   # show the length of string

# output:
#   s = Hello world!
#   type(s) = <type 'str'>
#   length = 12

